      SUBROUTINE HELAS_CALLS_UVCT_1(P,NHEL,H,IC)
C     
C     Modules
C     
      USE POLYNOMIAL_CONSTANTS
C     
      IMPLICIT NONE
C     
C     CONSTANTS
C     
      INTEGER    NEXTERNAL
      PARAMETER (NEXTERNAL=5)
      INTEGER    NCOMB
      PARAMETER (NCOMB=32)
      INTEGER NBORNAMPS
      PARAMETER (NBORNAMPS=6)
      INTEGER    NLOOPS, NLOOPGROUPS, NCTAMPS
      PARAMETER (NLOOPS=49, NLOOPGROUPS=15, NCTAMPS=115)
      INTEGER    NLOOPAMPS
      PARAMETER (NLOOPAMPS=164)
      INTEGER    NWAVEFUNCS,NLOOPWAVEFUNCS
      PARAMETER (NWAVEFUNCS=17,NLOOPWAVEFUNCS=95)
      REAL*8     ZERO
      PARAMETER (ZERO=0D0)
      REAL*16     MP__ZERO
      PARAMETER (MP__ZERO=0.0E0_16)
C     These are constants related to the split orders
      INTEGER    NSO, NSQUAREDSO, NAMPSO
      PARAMETER (NSO=2, NSQUAREDSO=1, NAMPSO=2)
C     
C     ARGUMENTS
C     
      REAL*8 P(0:3,NEXTERNAL)
      INTEGER NHEL(NEXTERNAL), IC(NEXTERNAL)
      INTEGER H
C     
C     LOCAL VARIABLES
C     
      INTEGER I,J,K
      COMPLEX*16 COEFS(MAXLWFSIZE,0:VERTEXMAXCOEFS-1,MAXLWFSIZE)

      LOGICAL DUMMYFALSE
      DATA DUMMYFALSE/.FALSE./
C     
C     GLOBAL VARIABLES
C     

      INCLUDE 'coupl.inc'
      INCLUDE 'mp_coupl.inc'

      INTEGER HELOFFSET
      INTEGER GOODHEL(NCOMB)
      LOGICAL GOODAMP(NSQUAREDSO,NLOOPGROUPS)
      COMMON/FILTERS/GOODAMP,GOODHEL,HELOFFSET

      LOGICAL CHECKPHASE
      LOGICAL HELDOUBLECHECKED
      COMMON/INIT/CHECKPHASE, HELDOUBLECHECKED

      INTEGER SQSO_TARGET
      COMMON/SOCHOICE/SQSO_TARGET

      LOGICAL UVCT_REQ_SO_DONE,MP_UVCT_REQ_SO_DONE,CT_REQ_SO_DONE
     $ ,MP_CT_REQ_SO_DONE,LOOP_REQ_SO_DONE,MP_LOOP_REQ_SO_DONE
     $ ,CTCALL_REQ_SO_DONE,FILTER_SO
      COMMON/SO_REQS/UVCT_REQ_SO_DONE,MP_UVCT_REQ_SO_DONE
     $ ,CT_REQ_SO_DONE,MP_CT_REQ_SO_DONE,LOOP_REQ_SO_DONE
     $ ,MP_LOOP_REQ_SO_DONE,CTCALL_REQ_SO_DONE,FILTER_SO

      INTEGER I_SO
      COMMON/I_SO/I_SO
      INTEGER I_LIB
      COMMON/I_LIB/I_LIB

      COMPLEX*16 AMP(NBORNAMPS)
      COMMON/AMPS/AMP
      COMPLEX*16 W(20,NWAVEFUNCS)
      COMMON/W/W

      COMPLEX*16 WL(MAXLWFSIZE,0:LOOPMAXCOEFS-1,MAXLWFSIZE,
     $ -1:NLOOPWAVEFUNCS)
      COMPLEX*16 PL(0:3,-1:NLOOPWAVEFUNCS)
      COMMON/WL/WL,PL

      COMPLEX*16 AMPL(3,NCTAMPS)
      COMMON/AMPL/AMPL

C     
C     ----------
C     BEGIN CODE
C     ----------

C     The target squared split order contribution is already reached
C      if true.
      IF (FILTER_SO.AND.UVCT_REQ_SO_DONE) THEN
        GOTO 1001
      ENDIF

C     Amplitude(s) for UVCT diagram with ID 56
      CALL FFV1_0(W(1,5),W(1,6),W(1,7),GC_2,AMPL(2,104))
      AMPL(2,104)=AMPL(2,104)*(1.0D0*UVWFCT_G_2_1EPS+1.0D0
     $ *UVWFCT_G_1_1EPS+1.0D0*UVWFCT_G_0_1EPS+2.0D0*UVWFCT_T_0_1EPS)
C     Amplitude(s) for UVCT diagram with ID 57
      CALL FFV1_0(W(1,5),W(1,6),W(1,7),GC_2,AMPL(1,105))
      AMPL(1,105)=AMPL(1,105)*(2.0D0*UVWFCT_T_0+1.0D0*UVWFCT_G_1+1.0D0
     $ *UVWFCT_G_2+1.0D0*UVWFCT_G_0)
C     Amplitude(s) for UVCT diagram with ID 58
      CALL FFS1_0(W(1,5),W(1,6),W(1,8),GC_37,AMPL(2,106))
      AMPL(2,106)=AMPL(2,106)*(1.0D0*UVWFCT_G_2_1EPS+1.0D0
     $ *UVWFCT_G_1_1EPS+1.0D0*UVWFCT_G_0_1EPS+2.0D0*UVWFCT_T_0_1EPS)
C     Amplitude(s) for UVCT diagram with ID 59
      CALL FFS1_0(W(1,5),W(1,6),W(1,8),GC_37,AMPL(1,107))
      AMPL(1,107)=AMPL(1,107)*(2.0D0*UVWFCT_T_0+1.0D0*UVWFCT_G_1+1.0D0
     $ *UVWFCT_G_2+1.0D0*UVWFCT_G_0)
C     Amplitude(s) for UVCT diagram with ID 60
      CALL FFV2_5_0(W(1,5),W(1,6),W(1,9),GC_22,GC_23,AMPL(2,108))
      AMPL(2,108)=AMPL(2,108)*(1.0D0*UVWFCT_G_2_1EPS+1.0D0
     $ *UVWFCT_G_1_1EPS+1.0D0*UVWFCT_G_0_1EPS+2.0D0*UVWFCT_T_0_1EPS)
C     Amplitude(s) for UVCT diagram with ID 61
      CALL FFV2_5_0(W(1,5),W(1,6),W(1,9),GC_22,GC_23,AMPL(1,109))
      AMPL(1,109)=AMPL(1,109)*(2.0D0*UVWFCT_T_0+1.0D0*UVWFCT_G_1+1.0D0
     $ *UVWFCT_G_2+1.0D0*UVWFCT_G_0)
C     Amplitude(s) for UVCT diagram with ID 62
      CALL FFV1_0(W(1,10),W(1,4),W(1,7),GC_2,AMPL(2,110))
      AMPL(2,110)=AMPL(2,110)*(1.0D0*UVWFCT_G_2_1EPS+1.0D0
     $ *UVWFCT_G_1_1EPS+1.0D0*UVWFCT_G_0_1EPS+2.0D0*UVWFCT_T_0_1EPS)
C     Amplitude(s) for UVCT diagram with ID 63
      CALL FFV1_0(W(1,10),W(1,4),W(1,7),GC_2,AMPL(1,111))
      AMPL(1,111)=AMPL(1,111)*(2.0D0*UVWFCT_T_0+1.0D0*UVWFCT_G_1+1.0D0
     $ *UVWFCT_G_2+1.0D0*UVWFCT_G_0)
C     Amplitude(s) for UVCT diagram with ID 64
      CALL FFS1_0(W(1,10),W(1,4),W(1,8),GC_37,AMPL(2,112))
      AMPL(2,112)=AMPL(2,112)*(1.0D0*UVWFCT_G_2_1EPS+1.0D0
     $ *UVWFCT_G_1_1EPS+1.0D0*UVWFCT_G_0_1EPS+2.0D0*UVWFCT_T_0_1EPS)
C     Amplitude(s) for UVCT diagram with ID 65
      CALL FFS1_0(W(1,10),W(1,4),W(1,8),GC_37,AMPL(1,113))
      AMPL(1,113)=AMPL(1,113)*(2.0D0*UVWFCT_T_0+1.0D0*UVWFCT_G_1+1.0D0
     $ *UVWFCT_G_2+1.0D0*UVWFCT_G_0)
C     Amplitude(s) for UVCT diagram with ID 66
      CALL FFV2_5_0(W(1,10),W(1,4),W(1,9),GC_22,GC_23,AMPL(2,114))
      AMPL(2,114)=AMPL(2,114)*(1.0D0*UVWFCT_G_2_1EPS+1.0D0
     $ *UVWFCT_G_1_1EPS+1.0D0*UVWFCT_G_0_1EPS+2.0D0*UVWFCT_T_0_1EPS)
C     Amplitude(s) for UVCT diagram with ID 67
      CALL FFV2_5_0(W(1,10),W(1,4),W(1,9),GC_22,GC_23,AMPL(1,115))
      AMPL(1,115)=AMPL(1,115)*(2.0D0*UVWFCT_T_0+1.0D0*UVWFCT_G_1+1.0D0
     $ *UVWFCT_G_2+1.0D0*UVWFCT_G_0)
C     At this point, all UVCT amps needed for (QCD=4 QED=4), i.e. of
C      split order ID=1, are computed.
      IF(FILTER_SO.AND.SQSO_TARGET.EQ.1) GOTO 3000

      GOTO 1001
 3000 CONTINUE
      UVCT_REQ_SO_DONE=.TRUE.
 1001 CONTINUE
      END

